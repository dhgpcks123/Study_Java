package day02;

public class Test11 {
	public static void main(String[] args) {
		int no= 10;
		no += 10; // no = no+10;
		System.out.println(no);
		//20
		no -= 2; // no= no-2;0
		System.out.println(no);
		//18
		no /=3 ; // no= no/3;
		System.out.println(no);
		//6
		no *=2 ; // no= no*2;
		System.out.println(no);
		//12
		no %=5 ; // no= no%5;
		System.out.println(no);
		//2
	}
}
